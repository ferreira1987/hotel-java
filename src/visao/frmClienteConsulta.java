    private void jmiAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmiAlterarActionPerformed

        if (this.tblCliente.getSelectedRowCount() > 0) {
            int row = this.tblCliente.getSelectedRow();
            int codCliente = (int) this.tblCliente.getValueAt(row, 0);

            frmCliente11.getInstancia().setVisible(true);
            frmCliente11.getInstancia().alterarCliente(codCliente);

            this.setVisible(false);
            this.dispose();
        }
    }//GEN-LAST:event_jmiAlterarActionPerformed

    private void txtConsultaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtConsultaKeyPressed
       if(evt.getKeyCode() == KeyEvent.VK_ENTER){
           this.searchCliente();
       }
    }//GEN-LAST:event_txtConsultaKeyPressed

    private void searchCliente() {
        String value = txtConsulta.getText();
        ClienteDao client = new ClienteDao();
        ArrayList<Cliente> listClient = client.consultClient(value);

        Object[] columns = {"C처digo", "Nome / Raz찾o", "Nome / Fantasia", "CPF / CNPJ"};
        DefaultTableModel model = new DefaultTableModel(new Object[0][0], columns);

        for (Cliente cc : listClient) {
            Object[] obj = new Object[4];
            obj[0] = cc.getCodigo();
            obj[1] = cc.getNome();
            obj[2] = cc.getSobrenome();
            obj[3] = cc.getCpfCnpj();
            model.addRow(obj);
        }

        this.tblCliente.setModel(model);
        this.tblCliente.getColumnModel().getColumn(0).setPreferredWidth(5);
        this.tblCliente.getColumnModel().getColumn(1).setPreferredWidth(100);
        this.tblCliente.getColumnModel().getColumn(2).setPreferredWidth(50);
        this.tblCliente.getColumnModel().getColumn(3).setPreferredWidth(30);
    }

