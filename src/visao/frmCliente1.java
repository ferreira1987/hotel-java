    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        int type = this.tabDados.getSelectedIndex();
        boolean validaPessoa = (type == 0 ? this.validaPessoaFisica() : this.validaPessoaJuridica());
        boolean enderecoContato = this.validaEnderecoContato();

        if (validaPessoa && enderecoContato) {
            Cliente client = (type == 0 ? this.setPessoaFisica() : this.setPessoaJuridica());
            ClienteDao dao = new ClienteDao();
            dao.incluir(client);

            if (dao.getResult() > 1) {
                boolean contato = this.setContato(dao.getResult());
                boolean endereco = this.setEndereco(dao.getResult());

                if (contato && endereco) {
                    this.clearFields();
                    this.disabledFields();
                    this.botaoEnabled(true, false, true, false, false, false);
                    JOptionPane.showMessageDialog(null, "Cliente cadastro com sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Ocorreu um erro ao gravar o Endereço:\n" + dao.getError(), "Error", JOptionPane.ERROR_MESSAGE);
                    this.msg = "";
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro ao cadastrar o Cliente:\n" + dao.getError(), "Error", JOptionPane.ERROR_MESSAGE);
                this.msg = "";
            }

        } else {
            JOptionPane.showMessageDialog(null, this.msg, "Atenção", JOptionPane.WARNING_MESSAGE);
            this.msg = "";
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

