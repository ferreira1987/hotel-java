    private boolean setContato(int codCliente) {
        ContatoCliente cc = new ContatoCliente();
        cc.setCodCliente(codCliente);
        cc.setTelefone(txtTelefone.getText());
        cc.setCelular(txtCelular.getText());
        cc.setEmail(txtEmail.getText());
        cc.setSite(txtSite.getText());

        ContatoClienteDao ccDao = new ContatoClienteDao();
        ccDao.incluir(cc);

        if (ccDao.getResult() > 0) {
            return true;
        } else {
            this.msg += ccDao.getError() + "\n";
            return false;
        }
    }

    private boolean setEndereco(int codCliente) {
        Endereco endereco = new Endereco();
        endereco.setCodCliente(codCliente);
        endereco.setLogradouro(txtLogradouro.getText());
        endereco.setNumero(txtNumero.getText());
        endereco.setComplemento(txtComplemento.getText());
        endereco.setBairro(txtBairro.getText());
        endereco.setCidade(txtCidade.getText());
        endereco.setCep(txtCep.getText());
        endereco.setEstado(cmbEstado.getSelectedItem().toString());
        endereco.setTipo(cmbTipoEndereco.getSelectedIndex());

        EnderecoDao enderecoDao = new EnderecoDao();
        enderecoDao.incluir(endereco);

        if (enderecoDao.getResult() > 0) {
            return true;
        } else {
            this.msg += enderecoDao.getError() + "\n";
            return false;
        }
    }

